                            <em>02</em>
                            <h4>Types of Systematic Investment Plans:</h4>
                            <p>
                                Below are the different kinds of systematic investment plans you can consider investing in:
                                <br />
                                •<strong>Top-up SIP:</strong>This kind of systematic investment plan enables you to increase your investment amount on a periodic basis while also giving you the flexibility to invest more when you have a higher income that can be invested. This type of SIP also helps one make the most out of their investments by investing in the best as well as the most high performing funds at regular intervals.
                                <br />
                                •<strong>Flexible SIP:</strong> As suggested from its name, this type of systematic investment plan carries with it the flexibility of the amount you want to invest in. The amount that is to be invested as per an investor’s cash flow and needs or preferences can be increased or decreased.
                                <br />
                                • <strong>Perpetual SIP:</strong> This type of SIP Plan enables you to carry out your investments without any end to the mandate date. Generally, a systematic investment plan carries an end date after one year, three years, or five years of investing. Hence, the investor has the freedom to withdraw the amount that is invested whether or not he wishes to invest in accordance with his financial goals.
