                            <em>03</em>
                            <h4>Benefits of SIP investing:</h4>
                            <small>Here are some of the several benefits when it comes to investing in an SIP over a lump sum investment. Some of them are listed below.</small>
                            <br />
                            <br />
                            • <strong>Makes you a more disciplined investor:</strong>In case you do not possess superior financial knowledge about the way in which the way the market moves, an SIP can make the ideal investment option. You need not spend your time analyzing market movements to find the right time to invest in something. With an SIP, your money is automatically deducted from your linked bank account and it goes towards your mutual funds.
                            <br />
                            • <strong>Rupee cost averaging:</strong> One of the main advantages of SIPs is rupee cost averaging. Since the amount you invest remains constant for a longer time period, with rupee cost averaging you can make the most of market volatility. The fixed amount that you choose to invest implies that your SIP will average out the value of each unit. Hence, you can choose to buy more units once the market is low and opt for lesser units when the markets are high. Ultimately, this will lower your average cost per unit.
                            <br />
                            • <strong>Power Of Compounding:</strong> SIPs allow the small amount you invest on a daily basis to grow into a large corpus as a sum of your contribution with returns that have compounded over the years. The power of compounding without the hassle of having to invest a lump sum amount gives SIPs a unique advantage over standard investment options.
