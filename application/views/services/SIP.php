                            <em>06</em>
                            <h4>Points you must consider while selecting your SIP frequency</h4>
                            <small>Monthly SIP, quarterly SIP, weekly SIP or daily SIP – you may be a little confused about the interval of SIPs you should opt for. You can decide it based on your income, expenses, ongoing EMIs, scheme you are investing in, financial goals, etc. However, you must ponder upon the following factors and their effect on the SIP frequency while deciding how often you want to invest in a particular SIP.</small>
                            <br />
                            <br />
                            <p>
                                1. <strong>Effect of volatility</strong> Depending on the volatility of the investment avenue, you should decide on the frequency of your SIP. If the volatility of the mutual fund or stock SIP is higher, you can invest in a higher frequency SIP so as to benefit from the market volatility by averaging out your investment over a period of time and vice-e-versa.
                                <br />
                                2. <strong>Frequency of your cash inflows:</strong>If you are a salaried professional and earn income on a monthly basis, it is advisable to invest in a monthly SIP. However, if you work on a contract basis where you receive income every few months (for example – construction business, interior designing, etc.) you can choose to invest in a SIP on a quarterly basis. You can plan to invest in weekly SIPs when you earn on a fortnight or a weekly basis (for example – internship, task-completion basis, etc.)
                                <br />
                                3. <strong>Ease in tracking installment payments</strong>It is easier to track your incomes and expenses monthly and thus, investment can be planned better on a monthly basis. You won’t have to keep track of how much was debited throughout the month as the installment amount will be debited once every month. On the other hand, in case of daily and weekly SIPs, the number of installments is higher leading to difficulty in keeping track of all the payments.
