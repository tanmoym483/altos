                            <em>07</em>
                            <h4>Common Myths about SIPs:</h4>
                            
                            <p><strong>Not for Large Investors:</strong> There is a common misconception that SIP plans are only for small investors. However SIP has got more to do with the frequency of payments rather than the total amount. Anybody can invest in a SIP plan, and up to Rs. 1 lakh can be invested provided your KYC process has been completed.
                            <br />
                            <p><strong>Don’t Invest in SIP When Market is Bullish:</strong>A bullish run occurs when the market is showing an upward trend, and many myths suggest that you must not invest in SIP when the market is being bullish. However, this is a baseless myth since an SIP relies on the rupee cost averaging to guarantee investors their returns and the rupee cost averaging works well over the long-term period, regardless of market conditions.
                            <p><strong>SIPs are not Flexible:</strong>Another common myth around the SIP investment plan is that this investment instrument is less flexible for investors as compared to other instruments. There are misconceptions that the tenure of the SIP plan or the amount invested in it cannot be changed. Another misconception is that having once invested in a SIP investment, you will not be allowed to discontinue it.
                            <br />
                               None of these statements are true. An SIP is among the most flexible investment instruments available to people, and one of the easiest to customize according to your specific requirements. With an SIP, you can easily alter the amount you have invested in it, and the tenure for which you are investing. Unlike many instruments which charge you for making these changes to an existing investment, an SIP investment has no penalties for making these changes.
                               <br />
                                    However, some SIP plans do have restrictions regarding the minimum amount that you can invest and the minimal amount of time that you must invest for, in order to be able to reap any rewards at all. Make sure to ask questions about this before you sign on for any particular SIP, to ensure that you understand all the important terms and conditions of your investment.
                                    <p><strong>Returns are Guaranteed:</strong>In line with the rising popularity of the instrument, several myths have come up surrounding SIPs which are seeing a lot of acclaim from across different demographic sets of the population. One common misconception has been that investing in a SIP will guarantee you returns, since your funds are invested periodically into the same nature of investment.
                                    <br />
                                    While no investment can ever guarantee you profitable returns, however, if you do invest in SIP, you stand a better chance of earning returns than through a more directly market-linked instrument. This is again owing to the rupee cost averaging principle, which enables you to offset the market volatility by remaining invested for a longer period of time.
                                    <p><strong>Only for Equity Markets:</strong>The dearth of knowledge in the common arena about investment instruments is evident when you realise that some of the myths are quite contradictory to each other. A common misconception around SIPs is that they invest only on equity stocks or in the market. This misconception deepens into mistrust, since the equity markets are notorious for being volatile and affected by a range of issues, including political, economical, and social, both locally and globally.
                                    <br />
                                    This makes several investors wary because it significantly reduces the chances of earning profitable returns. However, this is not true. The fact is that you can choose which kind of security you would like to invest in, through your SIP investment plan. Based on your goal, requirement as well as risk profile, you can take the call on which kind of security you wish to invest in and thus, have control over your funds.
