                            <h4>Taxation of SWP mutual funds</h4>
                            <p>Withdrawals from SWP are subject to taxation. The capital gain realised from investing in debt funds for less than 36 months will get added to the total income. If the investment period stretches over 3 years, a 20 percent capital gain tax will apply after indexation.
                            <br />
                            In the case of equity funds, a 15 percent short-term capital gain tax will apply for a holding period of less than a year. On the other hand, if you invest over a year, the applied tax rate is 10 percent without indexation.
