                                • <strong>Regular income:</strong>SWP lets mutual funds investors receive a steady income from their investment. Hence, investors needing a stable cash flow for meeting everyday expenses, like the retired investors, can invest in SWP schemes.
                                <br />
                                • <strong>Capital appreciation:</strong> The regular withdrawal is less than the returns earned from the investment. Hence, the investors get some capital appreciation in the long run.
                                <br />
                                • <strong>No TDS:</strong>There is no TDS deducted on SWP for resident investors
                               

