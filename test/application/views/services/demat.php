            <h2>What is a Demat Account?</h2>
            <p>
                Demat Account or Dematerialisation Account is used to electronically store your investments like Shares, Mutual Funds, ETFs, Bonds,
                Government securities etc without any paperwork.
                <br />
                Initially, stocks and shares used to be exchanged via physical receipts called certificates. This included lengthy paperwork and took up a
                lot of time. To counter this and to take advantage of an electronic trading platform which was gaining traction in West and the Asian Markets,
                the process of dematerialisation (demat) of shares was initiated in 1996. Physical share certificates were
                converted into electronic form securities of equivalent number and price and
                were credited to the investor's demat account. Thus, the advent of trading commenced this way.
            </p>
