                            <em>02</em>
                            <h4>Common Terms of a Demat Account</h4>
                            <small>While using a Demat Account, you will come across the below terms often:</small>
                            <br />
                            <br />
                            • <strong>Depository: </strong>At present, two depositories are registered with SEBI (i) CDSL - Central Depository of Securities Ltd. (ii) NSDL - National Depository of Securities Ltd. These depositories hold your demat account on your behalf. The work is similar to that of a bank.
                            <br />
                            • <strong>Depository Participant(s)(DP): </strong> All investors deal with the depository (CDSL/NSDL) through an agent. Financial institutions, scheduled commercial banks, foreign banks having operations in India approved by RBI (Reserve bank of India), stock brokers, clearing corporations or houses, state financial corporations, NBFCs, share transfer agent, etc., which comply with the conditions proposed by SEBI can be registered as a DP. Banking services can be availed through a branch; similarly depository services can be availed via a DP.
                            <br />
                            • <strong>Portfolio Holding:</strong> The demat account enables you to view your portfolio holdings of investments every time a transaction is undertaken. Since it is an automated process every purchase and sale is recorded with the minutest details of price, date of purchase or date of purchase, quantity etc.
                            <br />
                            • <strong>Unique ID:</strong> Every single demat account has a unique verification identity number. This is used during transactions to aid the companies to identify and credit the securities in the specific investor's account. It is a unique 16 digit number, the first eight digits are the DP id, and the last 8 are the Client Id ( investor account).

