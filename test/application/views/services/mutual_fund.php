                            <em>03</em>
                            <h4>What are the benefits and risks of mutual funds?</h4>
                            <small>Mutual funds offer professional investment management and potential diversification. They also offer three ways to earn money:</small>
                            <br>
                            <br>
                            <ul>
                                <li>• Dividend Payments. A fund may earn income from dividends on stock or interest on bonds. The fund then pays the shareholders nearly all the income, less expenses.</li>
                                <li>• Capital Gains Distributions. The price of the securities in a fund may increase. When a fund sells a security that has increased in price, the fund has a capital gain. At the end of the year, the fund distributes these capital gains, minus any capital losses, to investors.</li>
                                <li>• Increased NAV. If the market value of a fund’s portfolio increases, after deducting expenses, then the value of the fund and its shares increases. The higher NAV reflects the higher value of your investment.</li>
                            </ul>
                            <br>
                            <p>All funds carry some level of risk. With mutual funds, you may lose some or all of the money you invest because the securities held by a fund can go down in value. Dividends or interest payments may also change as market conditions change.</p>
                            <p>A fund’s past performance is not as important as you might think because past performance does not predict future returns. But past performance can tell you how volatile or stable a fund has been over a period of time. The more volatile the fund, the higher the investment risk.</p>
