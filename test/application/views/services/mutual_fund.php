                            <em>02</em>
                            <h4>What types of mutual funds are there?</h4>
                            <small>Most mutual funds fall into one of four main categories – money market funds, bond funds, stock funds, and target date funds. Each type has different features, risks, and rewards.</small>
                            <br />
                            <br />
                            <ul>
                                <li>• Money market funds have relatively low risks. By law, they can invest only in certain high-quality, short-term investments issued by U.S. corporations, and federal, state and local governments.</li>
                                <li>• Bond funds have higher risks than money market funds because they typically aim to produce higher returns. Because there are many different types of bonds, the risks and rewards of bond funds can vary dramatically.</li>
                                <li>• Stock funds invest in corporate stocks. Not all stock funds are the same. Some examples are:
                                    <ul>
                                        <li>Growth funds focus on stocks that may not pay a regular dividend but have
                                            potential for above-average financial gains.
                                        </li>
                                        <li>Income funds invest in stocks that pay regular dividends.</li>
                                        <li>Index funds track a particular market index such as the Standard & Poor’s 500 Index.</li>
                                        <li>Sector funds specialize in a particular industry segment.</li>
                                    </ul>
                                </li>
                                <li>• Target date funds hold a mix of stocks, bonds, and other investments. Over time, the mix gradually shifts according to the fund’s strategy. Target date funds, sometimes known as lifecycle funds, are designed for individuals with particular retirement dates in mind.</li>
                            </ul>
