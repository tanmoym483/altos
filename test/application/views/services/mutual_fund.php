            <h2>What are mutual funds?</h2>
            <p>
                A mutual fund is a company that pools money from many investors and invests the money in securities such as stocks,
                bonds, and short-term debt. The combined holdings of the mutual fund are known as its portfolio. Investors buy shares
                in mutual funds. Each share represents an investor’s part ownership in the fund and the income it generates.
